function config = get_data_params(numFftFeatures, scriptDir, outputName, overrides)
%% Return shared configuration for data generation scripts.
%   config = GET_DATA_PARAMS(numFftFeatures, scriptDir, outputName, overrides)
%   builds the parameter set and derived values used across the data
%   generation scripts. Provide overrides as a struct to adjust defaults.

    if nargin < 4
        overrides = struct();
    end

    defaults = struct(...
        'Nsym', 1000, ...
        'Nfft', 100, ...
        'beta', 0.25, ...
        'span', 10, ...
        'osr', 16, ...
        'Ts', 1, ...
        'avoid', 10, ...
        'a', 1.4678, ...
        'a1', 1, ...
        'a3', -2.5261, ...
        'noiseVariance', 0.1, ...
        'includeNoise', true, ...
        'numRuns', 2, ...
        'maxAnalysisFreq', 5, ...
        'referenceMaxBins', 60);

    overrideFields = fieldnames(overrides);
    for idx = 1:numel(overrideFields)
        name = overrideFields{idx};
        if isfield(defaults, name)
            defaults.(name) = overrides.(name);
        end
    end

    params = struct(...
        'Nsym', defaults.Nsym, ...
        'Nfft', defaults.Nfft, ...
        'osr', defaults.osr, ...
        'avoid', defaults.avoid, ...
        'a', defaults.a, ...
        'a1', defaults.a1, ...
        'a3', defaults.a3, ...
        'noiseVar', defaults.noiseVariance);

    pulse = rcosdesign(defaults.beta, defaults.span, defaults.osr, 'sqrt');

    T = defaults.Ts / defaults.osr;
    fs = 1 / T;
    chunk = defaults.Nsym * defaults.osr;

    freqAxis = (0:chunk-1) * (fs / chunk);
    analysisMask = freqAxis <= defaults.maxAnalysisFreq;
    analysisFreqs = freqAxis(analysisMask);

    [targetFreqs, fftSampleIdx] = get_fft_reference_selection(...
        numFftFeatures, fs, chunk, defaults.maxAnalysisFreq, defaults.referenceMaxBins);

    config = struct();
    config.params = params;
    config.pulse = pulse;
    config.fs = fs;
    config.chunk = chunk;
    config.freqAxis = freqAxis;
    config.analysisMask = analysisMask;
    config.analysisFreqs = analysisFreqs;
    config.targetFreqs = targetFreqs;
    config.fftSampleIdx = fftSampleIdx;
    config.numRuns = defaults.numRuns;
    config.includeNoise = defaults.includeNoise;
    config.outputFile = fullfile(scriptDir, outputName);
    config.maxAnalysisFreq = defaults.maxAnalysisFreq;
    config.referenceMaxBins = defaults.referenceMaxBins;
    config.numFftFeatures = numFftFeatures;
end
